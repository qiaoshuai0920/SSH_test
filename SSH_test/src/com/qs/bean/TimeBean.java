package com.qs.bean;

public class TimeBean {
	
	private String startMonth;
	private String startDayth;
	private String endMonth;
	private String endDayth;
	private String timeStr;
	
	public String getStartMonth() {
		return startMonth;
	}
	public void setStartMonth(String startMonth) {
		this.startMonth = startMonth;
	}
	public String getStartDayth() {
		return startDayth;
	}
	public void setStartDayth(String startDayth) {
		this.startDayth = startDayth;
	}
	public String getEndMonth() {
		return endMonth;
	}
	public void setEndMonth(String endMonth) {
		this.endMonth = endMonth;
	}
	public String getEndDayth() {
		return endDayth;
	}
	public void setEndDayth(String endDayth) {
		this.endDayth = endDayth;
	}
	public String getTimeStr() {
		return timeStr;
	}
	public void setTimeStr(String timeStr) {
		this.timeStr = timeStr;
	}
	
	
}
